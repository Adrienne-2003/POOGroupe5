    public void aleatoire() {
        
        Random rc = new Random();
        String a = "ELECT"+rc.nextInt(999999999);
        
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306"
                    + "/gestioninscriptioncitoyen", "root", "");
            PreparedStatement ps = con.prepareStatement("SELECT numeroinscription "
                    + "FROM formulairee");
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                do{
                    a = "ELECT"+rc.nextInt(999999999);
                }while(rs.getString("numeroinscription").equalsIgnoreCase(a));     
            }
        } catch (SQLException ex) {
            Logger.getLogger(Formulaire.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        numeroinscription.setText(a);
    }

